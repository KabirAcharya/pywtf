function mapCharacters(csvData) {
  const charMap = {};
  const rows = csvData.trim().split('\n');
  rows.slice(1).forEach(row => {
    const [char, func] = row.split(',');
    if (char && func) {
      charMap[char] = func;
    }
  });
  return charMap
}

const charMap = mapCharacters(csvData);
const charMapPeriods = mapCharacters(csvDataPeriods);

function replaceAndJoin(input,eval,periods) {
  const asciiValues = Array.from(input, char => char.charCodeAt(0));
  let out = periods ? asciiValues
    .map(char => charMapPeriods[char] || `  <COULDNT FIND ${char}>  `)
    .join('+') : asciiValues
    .map(char => charMap[char] || `  <COULDNT FIND ${char}>  `)
    .join('+');
  return eval ? `exec(${out})` : out;
}
